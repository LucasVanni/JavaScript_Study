// Número do PI
console.log(Math.PI)

// Arredondando os números
console.log(Math.round(3.57))

// Forçando o arredondamento para baixo
console.log(Math.floor(3.99))

// Forçando o arredondamento para cima
console.log(Math.ceil(5.1))

// Número absoluto
console.log(Math.abs(-9.5))

// Valor mínimo
console.log(Math.min(10, 20 ,30, 5, 2, 4))

// Valor máximo
console.log(Math.max(10, 20 ,30, 5, 2, 4))

// Valor aleatório entre 0 e 1
console.log(Math.random())

// Para pegar um número aleatório basta fazer o seguinte
// Pega um número entre 0 e 100...
console.log(Math.random() * 100);

// Para garantir que será até 100 mesmo é adicionado o Math.floor
console.log(Math.floor(Math.random() * 100))

